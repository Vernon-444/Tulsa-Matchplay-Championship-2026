-- Run this in Supabase SQL Editor to create the table and RLS policies.
-- Table stores one row per recorded match result (winner); order by id gives application order.

create table if not exists public.match_results (
  id bigint generated by default as identity primary key,
  match_id int not null,
  winner_slot int not null check (winner_slot in (0, 1)),
  created_at timestamptz not null default now()
);

-- Allow anyone (including anon) to read results so the main bracket page can load.
alter table public.match_results enable row level security;

create policy "Allow public read"
  on public.match_results for select
  using (true);

-- Only authenticated users (admins) can insert or delete (undo).
create policy "Allow authenticated insert"
  on public.match_results for insert
  to authenticated
  with check (true);

create policy "Allow authenticated delete"
  on public.match_results for delete
  to authenticated
  using (true);

-- Optional: index for ordered fetch
create index if not exists match_results_order on public.match_results (id asc);
